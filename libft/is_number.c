#include "libft.h" 

/*============================================================================*/
/**
 * @brief Check if the given argument is a valid number.
 * 
 * @param str
 * @return int
 */

int	is_number(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (ft_isdigit(str[i]) == 0 || str[i] == '-' || str[i] == '+')
			return (0);
		i++;
	}
	return (1);
}

